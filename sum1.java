import java.util.*;
import java.lang.*;
import java.io.*;
class Ideone
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner s=new Scanner(System.in);
		int j=s.nextInt();
		int n[]=new int[6];
		int sum=0;
		int k=s.nextInt();
		for(int i=0;i<j;i++)
		{
			n[i]=s.nextInt();
		}
		for(int i=0;i<k;i++)
		{
			sum=sum=n[i];
		}
		System.out.println(sum);
	}
}
